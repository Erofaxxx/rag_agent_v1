# Большой клин-документ про закупки, ~5-7 chunks при CHUNK_SIZE=2400
CLEAN_PROCUREMENT = """\
# Положение о закупочной деятельности АО «Корпорация»

## 1. Общие положения

Настоящее Положение определяет порядок организации и проведения
закупочных процедур в АО «Корпорация» в соответствии с действующим
законодательством Российской Федерации и внутренними нормативными
актами Корпорации. Положение распространяется на все структурные
подразделения и обязательно к применению при любых закупках товаров,
работ и услуг для нужд Корпорации.

Целями закупочной деятельности являются: обеспечение производственных
и хозяйственных нужд Корпорации необходимыми товарами, работами и
услугами; эффективное расходование денежных средств; развитие
добросовестной конкуренции; предотвращение коррупционных проявлений
и иных злоупотреблений; обеспечение прозрачности процедур.

Закупочная деятельность Корпорации строится на принципах открытости
информации о закупках, равноправия участников, целевого и экономически
эффективного расходования денежных средств, отсутствия дискриминации
участников и неоправданных ограничений конкуренции по отношению к
участникам закупки. Указанные принципы конкретизируются в положениях
последующих разделов настоящего документа.

## 2. Способы закупок

Корпорация применяет следующие способы закупок: открытый конкурс,
закрытый конкурс, запрос предложений, запрос котировок, аукцион в
электронной форме, закупка у единственного поставщика. Выбор способа
осуществляется в зависимости от предмета закупки, начальной максимальной
цены договора и иных существенных условий, определённых пунктом 3.4
настоящего Положения.

Открытый конкурс применяется при начальной максимальной цене договора
свыше пяти миллионов рублей при отсутствии специальных оснований для
применения иного способа. Закрытый конкурс — при закупке товаров,
работ или услуг, сведения о которых составляют государственную или
коммерческую тайну. Запрос предложений и запрос котировок применяются
при сумме до пяти миллионов рублей. Закупка у единственного поставщика
допускается в исключительных случаях, перечень которых установлен
пунктом 5.2.

При проведении открытого конкурса извещение о закупке публикуется в
Единой информационной системе и на корпоративном сайте Корпорации.
Срок подачи заявок составляет не менее двадцати рабочих дней с момента
публикации извещения. Все поступившие заявки регистрируются в журнале
с присвоением входящего номера в порядке поступления.

## 3. Порядок планирования закупок

Закупки осуществляются в соответствии с годовым планом закупок,
утверждаемым Советом директоров до 31 декабря года, предшествующего
году исполнения. План закупок содержит наименование предмета закупки,
способ закупки, планируемую дату объявления и заключения договора,
обоснование начальной максимальной цены, а также источник финансирования.

Корректировки плана закупок допускаются по мотивированному
представлению руководителя структурного подразделения с обязательным
согласованием финансового директора и юридической службы. Изменения,
влекущие увеличение совокупного объёма закупок более чем на 10
процентов, требуют дополнительного утверждения Совета директоров.

При формировании плана закупок учитываются результаты исполнения
плана предыдущего периода, заключения внутреннего аудита, а также
прогноз потребностей подразделений на основе их операционных и
инвестиционных планов. Структурные подразделения представляют свои
заявки в закупочную комиссию не позднее 1 ноября года, предшествующего
плановому. Заявки оформляются в установленной форме с приложением
обоснования.

## 4. Заключение договоров

Договоры по результатам закупочных процедур подписываются
уполномоченным лицом со стороны Корпорации в порядке, установленном
доверенностью или приказом о распределении полномочий. Договорная
работа ведётся юридической службой совместно с инициатором закупки
и финансовой службой. Существенные условия договора, включая срок
поставки, условия оплаты и ответственность сторон, должны
соответствовать требованиям конкурсной документации.

Срок заключения договора с победителем закупочной процедуры составляет
не более десяти рабочих дней с момента подведения итогов. В случае
отказа победителя от заключения договора закупочная комиссия вправе
заключить договор с участником, занявшим второе место в рейтинге
заявок, без проведения повторной процедуры.

Гарантия исполнения обязательств по договору устанавливается в размере
от пяти до тридцати процентов начальной максимальной цены договора в
зависимости от категории закупки. Способы обеспечения: денежный залог
на счёте Корпорации, банковская гарантия от банка из утверждённого
перечня, поручительство третьего лица.

## 5. Контроль и отчётность

Контроль исполнения закупочных процедур и заключённых договоров
возложен на закупочную комиссию, которая представляет ежеквартальный
отчёт Совету директоров. Отчёт включает сведения о количестве
проведённых процедур, экономии бюджета закупок, нарушениях и
санкциях, применённых к контрагентам.

Внутренний аудит закупочной деятельности проводится не реже одного
раза в полугодие специализированной службой внутреннего аудита.
Аудит охватывает законность, экономичность и эффективность
заключённых договоров. Результаты аудита докладываются Ревизионной
комиссии и Совету директоров с рекомендациями по улучшению процессов.

## 6. Антикоррупционные меры

Все участники закупочного процесса со стороны Корпорации обязаны
соблюдать антикоррупционную политику и Кодекс деловой этики. Конфликт
интересов раскрывается письменно до начала рассмотрения заявок.
Сотрудник, имеющий конфликт интересов, отстраняется от участия в
данной закупке. Нарушения этой нормы квалифицируются как грубое
дисциплинарное нарушение.

Контракты с компаниями, аффилированными с членами органов управления
Корпорации, подлежат обязательному раскрытию в годовом отчёте и
дополнительному согласованию Советом директоров вне зависимости от
суммы. Перечень аффилированных компаний обновляется ежеквартально
секретарём Совета директоров.
"""


