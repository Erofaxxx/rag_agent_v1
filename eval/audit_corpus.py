def write_corpus(out_dir: Path) -> dict:
    """Раскидываем clean и attack документы по подпапкам, возвращаем
    манифест для аудита."""
    clean_dir = out_dir / "clean"
    attack_dir = out_dir / "attacks"
    clean_dir.mkdir(parents=True, exist_ok=True)
    attack_dir.mkdir(parents=True, exist_ok=True)

    manifest = {"clean": [], "attacks": []}

    for fname, text in CLEAN_DOCS:
        (clean_dir / fname).write_text(text, encoding="utf-8")
        manifest["clean"].append(fname)

    for fname, text, attack_class, expected_layer in collect_attacks():
        (attack_dir / fname).write_text(text, encoding="utf-8")
        manifest["attacks"].append({
            "filename": fname,
            "attack_class": attack_class,
            "expected_layer": expected_layer,
        })

    return manifest


